"""Независимый фактчек флагмана — «2FA» по данным.

Зачем отдельный модуль/проход: автор (Криейтор) СЛЕП к собственным цифрам — он уже «поверил» в
них, пока писал, поэтому его самоотчёт «я проверил» = та же рука. Настоящая вторая проверка —
НЕЗАВИСИМЫЙ свежий вызов (как фактчекер в редакции): видит только финальный пост + бриф-источники,
сверяет каждое проверяемое утверждение через web_search и выдаёт вердикт. Не правит текст и не
пишет пост. Цифры — самый дорогой класс ошибок (красная линия бренда), поэтому это на флагман.

Дёшево: модель — Sonnet (резолвится через runmode, в /test падает на дешёвую). Вызывается из
run_pipeline (авто, перед постановкой) и из бота командой /verify (по последнему драфту).
"""
from __future__ import annotations

from core import config, llm, runmode

# Серверный веб-поиск Anthropic для сверки (его выполняет Claude). max_uses — кап стоимости проверки.
VERIFY_WEB = {"type": "web_search_20250305", "name": "web_search", "max_uses": 10}

VERIFIER_SYSTEM = (
    "Ты — НЕЗАВИСИМЫЙ фактчекер канала KANAKI CRYPTO (роль «2FA»). Ты НЕ писал этот пост и НЕ "
    "доверяешь ему по умолчанию. Задача ОДНА: проверить достоверность ВСЕХ проверяемых утверждений — "
    "цифры, проценты, суммы, даты, связки «X сделал Y / в тот же день», цитаты. По каждому: сверь "
    "через web_search и/или с брифом-источниками ниже. Будь скептичен: пока не нашёл источник "
    "Тир-1/2 — считай «не подтверждено». Особое внимание — связкам «одновременно/в тот же день» и "
    "круглым/вирусным цифрам.\n\n"
    "Текст НЕ правь и пост НЕ пиши. Выведи СПИСОК, по одной строке на утверждение:\n"
    "  <✅|⚠️|❓|📝> «<утверждение из поста, коротко>» — <источник / в чём конфликт / почему не подтверждено>\n"
    "Легенда: ✅ подтверждено Тир-1/2; ⚠️ КОНФЛИКТ (укажи, что на самом деле в источниках и какой "
    "цифре верить); ❓ не подтверждено (нет надёжного источника); 📝 цитата дана СМЫСЛОМ, не дословно.\n"
    "В конце ДВЕ строки: «ИТОГ: N✅ / N⚠️ / N❓» и затем «СТАТУС: ЧИСТО» (если НИ ОДНОГО ⚠️/❓) либо "
    "«СТАТУС: ПРАВКИ» (если есть хоть один ⚠️ или ❓). Коротко и по делу, без воды."
)


def has_issues(verdict: str) -> bool:
    """Есть ли в вердикте замечания (конфликт/неподтверждено), требующие авто-правки."""
    v = verdict or ""
    if "СТАТУС: ПРАВКИ" in v:
        return True
    if "СТАТУС: ЧИСТО" in v:
        return False
    # фолбэк, если модель не дала статус-строку: ищем строки-замечания (начинаются с ⚠️/❓)
    return any(ln.strip().startswith(("⚠", "❓")) for ln in v.split("\n"))


def _latest(subdir: str) -> str:
    """Содержимое самого свежего .md в memory/<subdir>/ (или '')."""
    d = config.ROOT / "memory" / subdir
    if not d.exists():
        return ""
    files = sorted(d.glob("*.md"), key=lambda p: p.stat().st_mtime, reverse=True)
    return files[0].read_text(encoding="utf-8") if files else ""


def latest_brief() -> str:
    return _latest("briefs")


def latest_draft() -> str:
    return _latest("drafts")


def verify_post(post: str, brief: str = "", api_key: str | None = None, model: str | None = None) -> str:
    """Независимая проверка фактов поста. Возвращает вердикт-таблицу (текст для чата/лога)."""
    post = (post or "").split("[[SPLIT]]")[0].strip()  # только сам пост, без меты-заметки
    if not post:
        return "(нечего проверять — пустой пост)"
    mdl = model or runmode.resolve("claude-sonnet-4-6")
    user = (f"ПОСТ ДЛЯ ПРОВЕРКИ:\n{post}\n\n"
            f"ИСХОДНЫЙ БРИФ СКАУТА (источники для сверки):\n{brief or '(бриф не найден)'}\n\n"
            "Проверь КАЖДОЕ проверяемое утверждение. Выдай вердикт-список и ИТОГ.")
    try:
        text, _ = llm.reply(mdl, VERIFIER_SYSTEM, [], user, [VERIFY_WEB],
                            lambda _n, _a: "", api_key, {"type": "adaptive"})
        return (text or "(пусто)").strip()
    except Exception as e:  # noqa: BLE001 — фактчек не должен ронять конвейер
        return f"(фактчек не удался: {e})"

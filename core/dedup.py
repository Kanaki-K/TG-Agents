    "свежий повтор заметнее всего читателю. По КАЖДОМУ кандидату поставь ОДИН из трёх вердиктов:\n"
    "  🆕 — новая сущность/инфоповод, ЛИБО та же тема, но не поднималась давно (вне окна 2-3 недель).\n"

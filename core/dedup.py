def data_age_hours() -> float | None:
    """Возраст выгрузки канала (channel_posts.json) в часах. None — выгрузки ещё нет."""
    p = analytics.POSTS_JSON
    if not p.exists():
        return None
    return (time.time() - p.stat().st_mtime) / 3600.0


def refresh_if_stale(max_hours: float = CHANNEL_FRESH_HOURS) -> str:
    """Гейт актуальности: если выгрузка канала устарела — подтянуть свежие посты ДО сверки.

    Возвращает строку-статус для лога. Свежо → ничего не тянем. Тяга идёт через
    analytics.refresh_metrics (collect → enrich_topics и пр.); ошибки сборщика она глотает сама —
    конвейер не роняем, в худшем случае сверяемся по тому, что уже есть.
    """
    age = data_age_hours()
    if age is not None and age < max_hours:
        return f"✅ Выгрузка канала свежая ({age:.1f}ч < {max_hours:.0f}ч) — анти-повтор по актуальным данным."
    why = "выгрузки канала нет" if age is None else f"выгрузка устарела ({age:.1f}ч ≥ {max_hours:.0f}ч)"
    print(f"🔄 Актуализация данных канала: {why} — тяну свежие посты (collect + enrich_topics)...")
    res = analytics.refresh_metrics(full=False)
    fresh = data_age_hours()
    dedup_ready = fresh is not None and fresh < max_hours  # анти-повтору нужны только посты+темы
    had_errors = "ошибк" in res.lower()
    if dedup_ready and had_errors:
        # collect+enrich прошли (выгрузка свежая), но какой-то шаг упал (напр. build_table = Excel).
        # На сверку это НЕ влияет — но честно сигналим, а не маскируем зелёной галкой.
        head = ("✅ Свежие посты подтянуты — анти-повтор готов; ⚠️ один из шагов обновления упал "
                "(см. ниже, на сверку не влияет)")
    elif dedup_ready:
        head = "✅ Данные канала обновлены"
    else:
        head = "⚠️ Обновить не удалось — сверяюсь по тому, что есть"
    return f"{head}.\n{res}"


def check(brief: str, api_key: str | None = None, model: str | None = None) -> str:
    """Сверка направлений брифа с уже опубликованным. Возвращает вердикт-текст (с финальными
    строками РЕКОМЕНДУЮ/СТАТУС — их парсят recommended_theme/all_repeats). Конвейер не роняет:
    на любой сбой отдаёт мягкий «СТАТУС: ОК» с пустой темой (Криейтор берёт тему из брифа сам)."""
    brief = (brief or "").strip()
    if not brief:
        return "(брифа нет — нечего сверять на повтор)\nРЕКОМЕНДУЮ: «»\nСТАТУС: ОК"
    digest = analytics.topics_digest()
    mdl = model or runmode.resolve("claude-haiku-4-5")
    user = (f"НАПРАВЛЕНИЯ ИЗ СВЕЖЕГО БРИФА СКАУТА (кандидаты на пост):\n{brief}\n\n"
            f"УЖЕ ОПУБЛИКОВАНО НА КАНАЛЕ (сводка тем, свежие сверху):\n{digest}\n\n"
            "Сверь каждое направление с опубликованным и дай вердикт строго по форме.")
    try:
        text, _ = llm.reply(mdl, DEDUP_SYSTEM, [], user, [], lambda _n, _a: "", api_key, None)
        return (text or "(пусто)").strip()
    except Exception as e:  # noqa: BLE001 — анти-повтор не должен ронять конвейер
        return f"(анти-повтор не удался: {e})\nРЕКОМЕНДУЮ: «»\nСТАТУС: ОК"


def recommended_theme(verdict: str) -> str:
    """Тема из строки «РЕКОМЕНДУЮ: «...»» (пустая, если все повторы / не нашли)."""
    for ln in (verdict or "").splitlines():
        s = ln.strip()
        if s.upper().startswith("РЕКОМЕНДУЮ") or s.startswith("РЕКОМЕНДУЮ"):
            a, b = s.find("«"), s.rfind("»")
            theme = s[a + 1:b].strip() if a != -1 and b > a else s.split(":", 1)[-1].strip()
            return "" if theme.upper().startswith("ВСЕ ПОВТОРЫ") else theme
    return ""


def all_repeats(verdict: str) -> bool:
    """True, если КАЖДОЕ направление — повтор (пост выпускать нельзя). Сначала по строке СТАТУС,
    фолбэк — по наличию 🆕/🔁 в вердикте."""
    for ln in (verdict or "").splitlines():
        s = ln.strip()
        if s.startswith("СТАТУС"):
            up = s.upper()
            return "ПОВТОР" in up and "ОК" not in up
    lines = (verdict or "").splitlines()
    has_ok = any(("🆕" in l or "⚠" in l) for l in lines)  # 🆕 и ⚠️ — обе НЕ блокируют выпуск
    has_rep = any("🔁" in l for l in lines)
    return has_rep and not has_ok

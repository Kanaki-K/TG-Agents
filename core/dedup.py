    "пост про то, что на канале УЖЕ выходило. Тебе дают направления-кандидаты из брифа разведки и "
    "сводку тем ВСЕХ опубликованных постов (заголовок/тема/суть, свежие сверху, с ДАТАМИ).\n"
    "Сравнивай по СУТИ, не по словам: та же сущность/проект/событие/инфоповод = та же тема, даже "

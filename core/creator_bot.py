def _read(rel: str) -> str:
    p = config.ROOT / rel
    return p.read_text(encoding="utf-8") if p.exists() else ""


def _system() -> str:
    persona = config.load_agent(AGENT_NAME)["persona"]
    lessons = _read("memory/post_lessons.md") or "(пока пусто — учусь на правках владельца)"
    playbook = _read("memory/format_playbook.md") or (
        "(плейбук ещё не собран — Аналитик соберёт командой /playbook; пока опирайся на "
        "post_standard и данные через top_posts/by_dimension/themes_overview)")
    ctx = (
        "## 📕 ГЛАВНЫЙ МАНУАЛ — пиши СТРОГО по нему (memory/content_manual.md)\n"
        "Это бибилия контента: голос («Вы» с большой буквы), типографика (валюта ПОСЛЕ числа `30$`, "
        "тире обычные `-`, кавычки прямые `\"…\"`), структура флагмана (чередование Д-Зн), запретник "
        "AI-паттернов, размер флагмана 2200–2500 ЗНАКОВ, футер, иллюстрация. При конфликте с другими "
        "файлами — приоритет здесь. Перед сдачей прогони пост по запретнику (§7) и чек-листу (§8).\n"
        f"{_read('memory/content_manual.md')}\n\n"
        "## Канон бренда — аудитория и красные линии (memory/brand.md)\n"
        f"{_read('memory/brand.md')}\n\n"
        "## Стандарт поста — форматы и метрики канала (memory/post_standard.md)\n"
        f"{_read('memory/post_standard.md')}\n\n"
        "## Плейбук форматов от Аналитика — какой формат под что (memory/format_playbook.md)\n"
        f"{playbook}\n\n"
        "## Уроки из правок владельца — ПРИМЕНЯЙ к каждому посту (memory/post_lessons.md)\n"
        f"{lessons}\n\n"
        "## Сводка по каналу (контекст: что заходит)\n"
        f"{analytics.summary()}\n"
    )
    return llm.build_system(persona, ctx)


async def main() -> None:
    cfg = config.load_agent(AGENT_NAME)
    # адаптивное мышление — острее композиция и точнее соблюдение красных линий; включается в config.yaml
    thinking = {"type": "adaptive"} if cfg.get("thinking") == "adaptive" else None
    await agent_runtime.run(
        AGENT_NAME,
        tools_schema=list(creator_tools.TOOLS),
        dispatch=creator_tools.dispatch,
        system_builder=_system,
        welcome=WELCOME,
        commands=COMMANDS,
        thinking=thinking,
    )

def _read(rel: str) -> str:
    p = config.ROOT / rel
    return p.read_text(encoding="utf-8") if p.exists() else ""


def _system() -> str:
    """ЛЁГКИЙ контекст scope: персона + свой мануал + красные линии + уроки. БЕЗ флагман-мануала,
    БЕЗ блока обложки, БЕЗ плейбука — короткому это не нужно (дешевле и не тянет в флагман)."""
    persona = config.load_agent(AGENT_NAME)["persona"]
    lessons = _read("memory/scope_lessons.md") or "(пока пусто — учусь на правках владельца к коротким постам)"
    ctx = (
        "## 🔭 ТЫ ПИШЕШЬ КОРОТКИЙ АНАЛИТИЧЕСКИЙ ПОСТ «ПОД ПРИЦЕЛОМ»\n"
        "Это ОТДЕЛЬНЫЙ формат. Правила и анатомия ФЛАГМАНА (антитеза на весь пост, 💭/💡-разделы, "
        "2800–4096 знаков, 15–30 цифр, обложка) к тебе НЕ применяются. Картинку ты НЕ делаешь. "
        "Доставку в канал делает Публикатор — выдай ЧИСТЫЙ короткий текст.\n\n"
        "## 🎙 РЕМЕСЛО — ОБЩИЙ канон голоса/анти-ИИ/типографики/жирного/запретов (memory/voice_core.md)\n"
        "Это НЕ флагман-концепция, а БРЕНД-фундамент: применяется и к короткому. Соблюдай СТРОГО — "
        "особенно §2 (точка в конце строки НЕ ставится; тире `-`; кавычки `\"…\"`; валюта после числа) "
        "и §4 (анти-ИИ запретник). ИИшная подача = провал.\n"
        f"{_read('memory/voice_core.md')}\n\n"
        "## 📕 МАНУАЛ РУБРИКИ (КОНЦЕПЦИЯ короткого: длина/структура/эталоны) — пиши по нему (memory/scope_manual.md)\n"
        f"{_read('memory/scope_manual.md')}\n\n"
        "## Канон бренда — аудитория и красные линии (memory/brand.md)\n"
        f"{_read('memory/brand.md')}\n\n"
        "## Уроки из правок владельца к КОРОТКИМ постам — ПРИМЕНЯЙ (memory/scope_lessons.md)\n"
        f"{lessons}\n"
    )
    return llm.build_system(persona, ctx)


def _turn(user_text: str, model: str, key: str, thinking, tools: list = TOOLS) -> str:
    text, _ = llm.reply(model, _system(), [], user_text, tools, creator_tools.dispatch, key, thinking)
    return text or ""


def write(theme: str = "", avoid: str = "") -> str:
    """Сгенерировать 🔭-пост (своя модель/контекст) + обязательный 2FA-фактчек с правкой. Возвращает
    финальный пост (его текст уже в драфте через save_draft; публикует владелец /schedule или пайплайн).
    avoid — направления-повторы от анти-повтора: scope их НЕ берёт (баг 30.06 — взял x402 после поста 23.06)."""
    cfg = config.load_agent(AGENT_NAME)
    key = config.agent_api_key(cfg)
    thinking = {"type": "adaptive"} if cfg.get("thinking") == "adaptive" else None
    model = runmode.resolve(SCOPE_MODEL)
    task = TASK
    if avoid:  # запрет на уже вышедшие темы — scope повод выбирает сам, но не из повторов
        task += ("\n\nАНТИ-ПОВТОР (сверено со свежей выгрузкой канала): НЕ бери эти направления — они уже "
                 f"выходили на канале: {avoid}. Возьми ДРУГОЙ свежий повод по своему гейту важности+свежести.")
    if theme:
        task += f"\n\nТЕМА ОТ ВЛАДЕЛЬЦА: {theme} — пиши по ней."
    post = _turn(task, model, key, thinking)
    # 2FA обязателен (цифры — красная линия). Свой фактчек на Sonnet, правки — своей же моделью/контекстом.
    try:
        verdict = verify.verify_post(verify.latest_draft(), verify.latest_brief(), api_key=key)
        if verify.has_issues(verdict):
            logging.info("scope 2FA: есть замечания — правлю фактами")
            fixed = _turn(FIX.format(post=post.split("[[SPLIT]]")[0], verdict=verdict), model, key, thinking)
            post = fixed or post
    except Exception:
        logging.exception("scope 2FA не удался — пост не блокирую, отдаю как есть")
    return post



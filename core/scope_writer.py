"""🔭 «Под прицелом» — короткий аналитический пост. ОТДЕЛЬНАЯ ветка от флагмана.

Зачем отдельно (а не команда внутри флагман-Криейтера):
  1) дёшево — НЕ грузим флагман-мануал/обложку/плейбук в контекст (короткому это не нужно);
  2) гибко — свои правила (memory/scope_manual.md) и своя модель настраиваются независимо;
  3) чисто — флагман и scope не мешают друг другу.

«Руки» (save_draft/read_brief/publish/web_search) переиспользуем из creator_tools — дублировать
инструменты незачем. Картинку (make_image) и правку стандарта намеренно НЕ даём. Самодостаточно:
write() делает генерацию → обязательный 2FA-фактчек → правку фактов СВОЕЙ моделью → финал.
"""
import logging

from core import config, creator_tools, llm, runmode, verify

AGENT_NAME = "creator"            # голос автора тот же — переиспользуем персону Криейтера
SCOPE_MODEL = "claude-sonnet-4-6"  # база scope: дешевле флагман-Opus; в /test → Haiku через runmode
WEB_SEARCH_TOOL = {"type": "web_search_20250305", "name": "web_search", "max_uses": 6}

# Инструменты scope = «руки» Криейтера БЕЗ make_image (картинку не делаем) и без правки стандарта/аналитики.
_DROP = {"make_image", "propose_standard", "apply_standard", "by_dimension", "themes_overview", "record_lesson"}
TOOLS = [t for t in creator_tools.TOOLS if t.get("name") not in _DROP] + [WEB_SEARCH_TOOL]

TASK = (
    "Напиши короткий пост 🔭 «ПОД ПРИЦЕЛОМ» СТРОГО по своему мануалу (memory/scope_manual.md в контексте). "
    "1) МАТЕРИАЛ: read_brief('latest') — возьми направление с флагом 🔭 (Скаут пометил) или сильнейший "
    "ВАЖНЫЙ инвестрелевантный повод. Нет значимого повода — СКАЖИ это и НЕ высасывай из пальца. "
    "Владелец дал свои тезисы/картинку — РАЗВЕРНИ ИХ (формулировки береги), не сочиняй с нуля. "
    "2) ВЕРИФИКАЦИЯ ДО ПИСЬМА: ключевые цифры/даты сверь web_search; цифра только с прослеживанием до "
    "источника, иначе [ПРОВЕРИТЬ] или убери. В САМ ТЕКСТ НЕ вставляй <cite>/сноски/ссылки — пост чистый. "
    "3) ПИШИ по scope_manual: длина ~400–900 (потолок 1000), безлично; ПРИЁМЫ ПОДАЧИ берёшь (хук-сцена, "
    "ОДНА антитеза-стержень, история-как-аргумент, контраст-поворот, якорный **жирный**, мораль одной "
    "строкой, ритм), ФУРНИТУРУ флагмана НЕ берёшь (без разделов 💡/💭, без нумерованных списков, без "
    "ДАННЫЕ→МЕХАНИКА→МОРАЛЬ во весь рост, футер не обязателен). Угол — «что это значит долгосрочнику» + "
    "честный риск. Красные линии строго (BTC не проигравший, без политоты/России/сигналов). "
    "4) save_draft(kind='scope') — линтер вернёт правки/предупреждения (длина >1000, фурнитура 💡/💭); "
    "устрани и вызови save_draft ЕЩЁ РАЗ с ФИНАЛОМ. "
    "5) ВЫВОД В ЧАТ: сперва ТОЛЬКО финальный пост (без преамбулы), затем `[[SPLIT]]` и ОТДЕЛЬНОЙ строкой "
    "короткая заметка проверки (почему повод тянет на 🔭 + что сверить). Брифов нет — скажи, попроси /scan."
)

FIX = (
    "Независимый фактчек (2FA) нашёл замечания к твоему короткому посту. Исправь ТОЧЕЧНО, сохраняя голос, "
    "длину (~400–900) и формат scope (без флагман-фурнитуры):\n"
    "— ⚠️ конфликт → поставь ВЕРНОЕ значение (сверься web_search);\n"
    "— ❓ не подтверждено → убери цифру/утверждение ИЛИ смягчи, БЕЗ выдуманной точной цифры;\n"
    "— ✅ не трогай.\n\nПОСТ:\n{post}\n\nВЕРДИКТ ФАКТЧЕКА:\n{verdict}\n\n"
    "Внеси правки, вызови save_draft(kind='scope') с ФИНАЛОМ, выведи ТОЛЬКО финальный пост (без меты)."
)


def _read(rel: str) -> str:
    p = config.ROOT / rel
    return p.read_text(encoding="utf-8") if p.exists() else ""


def _system() -> str:
    """ЛЁГКИЙ контекст scope: персона + свой мануал + красные линии + уроки. БЕЗ флагман-мануала,
    БЕЗ блока обложки, БЕЗ плейбука — короткому это не нужно (дешевле и не тянет в флагман)."""
    persona = config.load_agent(AGENT_NAME)["persona"]
    lessons = _read("memory/post_lessons.md") or "(пока пусто — учусь на правках владельца)"
    ctx = (
        "## 🔭 ТЫ ПИШЕШЬ КОРОТКИЙ АНАЛИТИЧЕСКИЙ ПОСТ «ПОД ПРИЦЕЛОМ»\n"
        "Это ОТДЕЛЬНЫЙ формат. Правила и анатомия ФЛАГМАНА (антитеза на весь пост, 💭/💡-разделы, "
        "2800–4096 знаков, 15–30 цифр, обложка) к тебе НЕ применяются. Картинку ты НЕ делаешь. "
        "Доставку в канал делает Публикатор — выдай ЧИСТЫЙ короткий текст.\n\n"
        "## 📕 МАНУАЛ РУБРИКИ — пиши СТРОГО по нему (memory/scope_manual.md)\n"
        f"{_read('memory/scope_manual.md')}\n\n"
        "## Канон бренда — аудитория и красные линии (memory/brand.md)\n"
        f"{_read('memory/brand.md')}\n\n"
        "## Уроки из правок владельца — ПРИМЕНЯЙ\n"
        f"{lessons}\n"
    )
    return llm.build_system(persona, ctx)


def _turn(user_text: str, model: str, key: str, thinking) -> str:
    text, _ = llm.reply(model, _system(), [], user_text, TOOLS, creator_tools.dispatch, key, thinking)
    return text or ""


def write(theme: str = "") -> str:
    """Сгенерировать 🔭-пост (своя модель/контекст) + обязательный 2FA-фактчек с правкой. Возвращает
    финальный пост (его текст уже в драфте через save_draft; публикует владелец /schedule или пайплайн)."""
    cfg = config.load_agent(AGENT_NAME)
    key = config.agent_api_key(cfg)
    thinking = {"type": "adaptive"} if cfg.get("thinking") == "adaptive" else None
    model = runmode.resolve(SCOPE_MODEL)
    task = TASK if not theme else TASK + f"\n\nТЕМА ОТ ВЛАДЕЛЬЦА: {theme} — пиши по ней."
    post = _turn(task, model, key, thinking)
    # 2FA обязателен (цифры — красная линия). Свой фактчек на Sonnet, правки — своей же моделью/контекстом.
    try:
        verdict = verify.verify_post(verify.latest_draft(), verify.latest_brief(), api_key=key)
        if verify.has_issues(verdict):
            logging.info("scope 2FA: есть замечания — правлю фактами")
            fixed = _turn(FIX.format(post=post.split("[[SPLIT]]")[0], verdict=verdict), model, key, thinking)
            post = fixed or post
    except Exception:
        logging.exception("scope 2FA не удался — пост не блокирую, отдаю как есть")
    return post

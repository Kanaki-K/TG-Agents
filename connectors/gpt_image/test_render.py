def _load_post() -> tuple[str, str]:
    f = config.ROOT / "data" / "test_post.txt"
    if f.exists():
        lines = [ln for ln in f.read_text(encoding="utf-8").splitlines()]
        title = (lines[0].strip() if lines else "").strip()
        body = "\n".join(lines[1:]).strip()
        if title and body:
            return title, body
        print("⚠ data/test_post.txt есть, но в нём нет заголовка+текста — беру встроенный пример.")
    return SAMPLE_TITLE, SAMPLE_POST


if __name__ == "__main__":
    title, post_text = _load_post()
    prompt = creator_tools._build_image_prompt(title, post_text)  # тот же код, что у Криейтора
